package metrics

import (
	"context"
	"fmt"
	"github.com/Jeffail/gabs/v2"
	"net/http"
	"strconv"
	"sync"
	"time"
)

// open container is open for modification with SetProperty, AddCount, and IncrementCount.
// closed container is set by Emit or
type Metrics struct {
	open      *gabs.Container
	closed    *gabs.Container
	counters  map[string]int64
	floaters  map[string]float64
	timings   map[string]*TimingStats
	mu        sync.Mutex
	startTime time.Time
}

// Default counter metrics that are always emitted.
const (
	CounterKeyFault    = "fault"
	CounterKeyPanicked = "panicked"
)

// Reserved property keys.
const (
	ReservedKeyStartTime = "startTime"
	ReservedKeyEndTime   = "endTime"
	ReservedKeyTime      = "time"
	ReservedKeyCounters  = "counters"
	ReservedKeyFloaters  = "floaters"
	ReservedKeyTimings   = "timings"
)

// For Metrics.SetProperty.
var reservedKeys = map[string]bool{
	ReservedKeyCounters:  true,
	ReservedKeyFloaters:  true,
	ReservedKeyEndTime:   true,
	ReservedKeyStartTime: true,
	ReservedKeyTime:      true,
	ReservedKeyTimings:   true,
}

// Creates an empty Metrics instance. The current UTC time will be the startTime property.
func New() *Metrics {
	return NewWithStartTime(time.Now())
}

// A variant of New that allows caller to override the startTime property.
func NewWithStartTime(startTime time.Time) *Metrics {
	return &Metrics{
		open: gabs.New(),
		counters: map[string]int64{
			CounterKeyFault:    0,
			CounterKeyPanicked: 0,
		},
		floaters:  map[string]float64{},
		timings:   map[string]*TimingStats{},
		mu:        sync.Mutex{},
		startTime: startTime.UTC(),
	}
}

// Convenient method that panics if err is not nil.
func Must(m *Metrics, err error) *Metrics {
	if err != nil {
		panic(err)
	}
	return m
}

// Creates a Metrics instance with the specified properties.
func NewWithProperties(properties map[string]interface{}) (*Metrics, error) {
	startTime := time.Now().UTC()

	container := gabs.New()
	for k, v := range properties {
		if v == nil {
			continue
		}

		if _, err := container.Set(v, k); err != nil {
			return nil, err
		}
	}

	return &Metrics{
		open:      container,
		mu:        sync.Mutex{},
		startTime: startTime,
	}, nil
}

// Sets a property to be logged. A few keys are generated by the metrics instance and cannot be overridden, and thus
// will be ignored by this method: startTime, endTime, time, timings, and counters.
func (m *Metrics) SetProperty(key string, v interface{}) error {
	if v == nil || reservedKeys[key] {
		return nil
	}

	if err := m.checkLock(); err != nil {
		return err
	}
	defer m.mu.Unlock()

	if _, err := m.open.Set(v, key); err != nil {
		return err
	}

	return nil
}

// A panicky fluent variant of SetProperty.
func (m *Metrics) MustSetProperty(key string, v interface{}) *Metrics {
	if err := m.SetProperty(key, v); err != nil {
		panic(err)
	}
	return m
}

// Increases the counter of the specified key by some delta.
func (m *Metrics) AddCount(key string, delta int64) error {
	if err := m.checkLock(); err != nil {
		return err
	}
	defer m.mu.Unlock()

	m.counters[key] += delta
	return nil
}

// A panicky fluent variant of AddCount.
func (m *Metrics) MustAddCount(key string, delta int64) *Metrics {
	if err := m.AddCount(key, delta); err != nil {
		panic(err)
	}
	return m
}

// Increments the counter of the specified key by 1.
func (m *Metrics) IncrementCount(key string) error {
	if err := m.checkLock(); err != nil {
		return err
	}
	defer m.mu.Unlock()

	m.counters[key]++
	return nil
}

// A panicky fluent variant of func (m *Metrics) IncrementCount(key string) error {.
func (m *Metrics) MustIncrementCount(key string) *Metrics {
	if err := m.IncrementCount(key); err != nil {
		panic(err)
	}
	return m
}

