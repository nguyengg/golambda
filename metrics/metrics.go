// Add a timing metric.
func (m *Metrics) AddTiming(key string, duration time.Duration) error {
	if err := m.checkLock(); err != nil {
		return err
	}
	defer m.mu.Unlock()

	stats, ok := m.timings[key]
	if !ok {
		stats = NewTimingStats(duration)
		m.timings[key] = stats
		return nil
	}

	stats.Add(duration)
	return nil
}

// Add a timing metric.
func (m *Metrics) MustAddTiming(key string, duration time.Duration) *Metrics {
	if err := m.AddTiming(key, duration); err != nil {
		panic(err)
	}
	return m
}

// Increments the counter of CounterKeyFault by 1.
func (m *Metrics) Faulted() error {
	if err := m.checkLock(); err != nil {
		return err
	}
	defer m.mu.Unlock()

	m.counters[CounterKeyFault]++
	return nil
}

// Increments the counter of CounterKeyPanicked by 1.
func (m *Metrics) Panicked() error {
	if err := m.checkLock(); err != nil {
		return err
	}
	defer m.mu.Unlock()

	m.counters[CounterKeyPanicked]++
	return nil
}

// Returns a non-nil error if the Metrics instance is closed for modification.
func (m *Metrics) checkLock() error {
	m.mu.Lock()

	if m.closed != nil {
		return fmt.Errorf("metrics instance already closed")
	}
	return nil
}

// Closes the Metrics instance for modification.
// Sets endTime to the current UTC time which will be used in conjunction with startTime to compute the time property.
// Subsequent modifications to the Metrics instance will result in an error.
// Subsequent calls to Close will have no effect.
func (m *Metrics) Close() {
	m.CloseWithEndTime(time.Now().UTC())
}

// A variant of Close that allows caller to override the endTime property.
func (m *Metrics) CloseWithEndTime(endTime time.Time) {
	if m.closed != nil {
		return
	}

	m.mu.Lock()
	defer m.mu.Unlock()

	if m.closed != nil {
		return
	}

	c := gabs.New()
	_ = c.Merge(m.open)
	_, _ = c.Set(m.startTime.UnixNano()/int64(time.Millisecond), ReservedKeyStartTime)
	_, _ = c.Set(endTime.Format(http.TimeFormat), ReservedKeyEndTime)
	_, _ = c.Set(formatDuration(endTime.Sub(m.startTime)), ReservedKeyTime)

	if len(m.counters) != 0 {
		_, _ = c.Set(m.counters, ReservedKeyCounters)
	}

	if len(m.floaters) != 0 {
		_, _ = c.Set(m.floaters, ReservedKeyFloaters)
	}

	if len(m.timings) != 0 {
		for k, timing := range m.timings {
			o, err := c.Object(ReservedKeyTimings, k)
			if err != nil {
				continue
			}
			_, _ = o.Set(formatDuration(timing.Sum), "sum")
			_, _ = o.Set(formatDuration(timing.Min), "min")
			_, _ = o.Set(formatDuration(timing.Max), "max")
			_, _ = o.Set(timing.N, "n")
			_, _ = o.Set(formatDuration(timing.Sum/time.Duration(timing.N)), "avg")
		}
	}

	m.closed = c
}

// Returns whether the Metrics instance is closed for modification.
func (m *Metrics) IsClosed() bool {
	return m.closed != nil
}

// Closes the Metrics instance and emits the JSON string describing the instance.
// Subsequent calls will return the same JSON string.
func (m *Metrics) JSONString() string {
	m.Close()
	return m.closed.String()
}

// A variant of EmitJSON with indentation.
func (m *Metrics) JSONStringIndent(prefix string, indent string) string {
	m.Close()
	return m.closed.StringIndent(prefix, indent)
}

type key int

var metricsKey key

// Returns a new Context that carries the specified Metrics instance.
func NewContext(ctx context.Context, metrics *Metrics) context.Context {
	newCtx := context.WithValue(ctx, metricsKey, metrics)
	return newCtx
}

// Returns the Metrics value stored in context, if any.
func FromContext(ctx context.Context) (*Metrics, bool) {
	m, ok := ctx.Value(metricsKey).(*Metrics)
	return m, ok
}

// A panicky variant of FromContext.
func MustFromContext(ctx context.Context) *Metrics {
	m, ok := FromContext(ctx)
	if !ok {
		panic("no metrics context")
	}
	return m
}

func formatDuration(duration time.Duration) string {
	return strconv.FormatFloat(float64(duration.Nanoseconds())/float64(time.Millisecond), 'f', 3, 64) + " ms"
}
